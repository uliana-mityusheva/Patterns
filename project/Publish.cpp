#include "Publish.h"


void Publish::output_one(std::string one) {
    std::cout << one;
}

void Publish::output_two(std::string one, std::string two) {
    std::cout << one << two;
}

void Publish::output_three(std::string one, std::string two, std::string three) {
    std::cout << one << two << three;
}
